#vWrite a program that checks if a number entered by the user is positive, negative, or zero.
# Sample solution:

num = float(input("Enter a number: "))
if num > 0:
 print("The number is positive.")
elif num < 0:
 print("The number is negative.")
else:
 print("The number is zero.")
 